# =========================================================
# 基本関数
# =========================================================
def v2(n):
    if n == 0: return float('inf')
    c = 0
    while n % 2 == 0: n //= 2; c += 1
    return c

def syracuse(n):
    val = 3 * n + 1
    while val % 2 == 0: val //= 2
    return val

def im_size(k):
    """|Im(T mod 2^k)|"""
    if k % 2 == 1:
        return 3 * 2**(k-3)
    else:
        return (7 * 2**(k-3) - 2) // 3

def rho(k):
    """占有率 = |Im|/2^{k-1}"""
    return Fraction(im_size(k), 2**(k-1))

