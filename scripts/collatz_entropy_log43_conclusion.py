def all_preimages_T2(y):
    pre = set()
    pre.add(2 * y)
    if (2*y - 1) % 3 == 0:
        x = (2*y - 1) // 3
        if x > 0 and x % 2 == 1:
            pre.add(x)
    return pre

current = {1}
for n in range(1, 55):
    next_set = set()
    for x in current:
        for p in all_preimages_T2(x):
            next_set.add(p)
    current = next_set
    if len(current) > 1:
        rate = math.log(len(current)) / n
        step = math.log(len(current) / (len(current) if n == 1 else 0)) if n == 1 else 0
        if n <= 5 or n % 10 == 0 or n >= 50:
            print(f"  n={n:2d}: |T2^{{-n}}(1)| = {len(current):10d}, rate = {rate:.6f}")

print(f"\n  理論予測: rate -> log(4/3) = {LOG_43:.6f}")
print(f"  注: 収束は O(log(n)/n) 程度の速さ")

